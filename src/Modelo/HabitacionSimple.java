public class HabitacionSimple {
    private int numero;
    private String estado;

    public HabitacionSimple(int numero, String estado) {
        this.numero = numero;
        this.estado = estado;
    }

    public String getEstado() {
        return estado;
    }

    public void setEstado(String estado) {
        this.estado = estado;
    }

    public int getNumero() {
        return numero;
    }

    public void setNumero(int numero) {
        this.numero = numero;
    }
    
    public double Costo(){
        return 120000.0; 
    }

  
}
